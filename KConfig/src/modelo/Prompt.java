package modelo;


public class Prompt {

	private String condicionIf;
	private String prompt;
	
	
	public String getCondicionIf() {
		return condicionIf;
	}
	public void setCondicionIf(String condicionIf) {
		this.condicionIf = condicionIf;
	}
	public String getPrompt() {
		return prompt;
	}
	public void setPrompt(String prompt) {
		this.prompt = prompt;
	}
	
}
